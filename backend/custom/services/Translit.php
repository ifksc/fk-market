<?php

namespace App\Services;

use Illuminate\Support\Str;

/**
 * Транслитерация кириллицы в латиницу и нормализация строки в slug.
 * Общий помощник для генераторов slug товаров и категорий.
 */
class Translit
{
    /** Карта транслитерации кириллицы → латиница. */
    private const MAP = [
        'а' => 'a', 'б' => 'b', 'в' => 'v', 'г' => 'g', 'д' => 'd', 'е' => 'e',
        'ё' => 'e', 'ж' => 'zh', 'з' => 'z', 'и' => 'i', 'й' => 'y', 'к' => 'k',
        'л' => 'l', 'м' => 'm', 'н' => 'n', 'о' => 'o', 'п' => 'p', 'р' => 'r',
        'с' => 's', 'т' => 't', 'у' => 'u', 'ф' => 'f', 'х' => 'h', 'ц' => 'ts',
        'ч' => 'ch', 'ш' => 'sh', 'щ' => 'sch', 'ъ' => '', 'ы' => 'y', 'ь' => '',
        'э' => 'e', 'ю' => 'yu', 'я' => 'ya',
        // украинские/казахские буквы
        'і' => 'i', 'ї' => 'yi', 'є' => 'ye', 'ґ' => 'g', 'ә' => 'a', 'қ' => 'k',
        'ұ' => 'u', 'ң' => 'n', 'ғ' => 'g', 'ө' => 'o', 'һ' => 'h',
    ];

    /** Транслитерировать кириллицу и нормализовать строку в slug. */
    public static function slugify(string $name): string
    {
        return Str::slug(strtr(mb_strtolower(trim($name)), self::MAP));
    }
}
